#include <stdlib.h>
#include <stdio.h>

/* betty style doc for function main goes there */
/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */


int main(void)
	{
	char ch, chu;

	for (ch = 'a'; ch <= 'z'; ch++)
	{
	putchar(ch);
	}
	for (chu = 'A'; chu <= 'Z'; ch++)
	{
	putchar(chu);
	}
	putchar('\n');
return (0);

}
